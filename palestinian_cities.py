def get_city_name(city_id: int) -> str:
    """Get the Arabic name of a city by its ID."""
    return PALESTINIAN_CITIES.get(city_id, ("Unknown", 0, 0))[0]

def get_all_city_names() -> Dict[int, str]:
    """Get all city names as dictionary."""
    return {city_id: name for city_id, (name, _, _) in PALESTINIAN_CITIES.items()}

